#include <unistd.h>

int	ft_strncmp(char *s1, char*s2, unsigned int n)
{
	while (n > 0)
	{
		if (*s1 == *s2 && *s1 != '\0' && *s2 != '\0')
		{
			n--;
			s1++;
			s2++;
		}
		else
			return ((int)*s1 - (int)*s2);
	}
	return ((int)*s1 - (int)*s2);
}

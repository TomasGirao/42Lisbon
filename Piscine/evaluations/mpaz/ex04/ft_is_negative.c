#include <unistd.h>

void	ft_is_negative(int n)
{
	char	neg;
	char	pos;

	neg = 'N' ;
	pos = 'P' ;
	if (n < 0)
	{
		write (1, "N", 1);
	}
	else 
	{
		write(1, "P", 1);
	}
}

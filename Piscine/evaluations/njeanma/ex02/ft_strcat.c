char	*ft_strcat(char *dest, char *src)
{
	int	i;
	int	f;

	i = 0;
	f = 0;
	while (dest[i] != '\0')
	{
		i++;
	}
	while (src[f] != '\0')
	{
		dest[i] = src[f];
		i++;
		f++;
	}
	dest[i] = '\0';
	return (dest);
}
